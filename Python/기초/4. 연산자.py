# 실습 1 대학생의 용돈 관리
'''
1. 시작용돈 30만원
2. 개강 첫 주 8만원 도서 구입
3. 평일 점심 값 매일 9천원씩 5일간 사용
4. 과외 아르바이트 12만원 수입
5. 부모님의 용돈으로 현재 급액의 20% 추가 수입
6. 전기요금 등의 공과금으로 남은 돈의 1/3 지출
'''
money = 0


def calMoney(money):
    ten_th = money // 10000
    one_th = (money % 10000) // 1000
    if (one_th == 0):
        one_th = "원"
    else:
        one_th = " " + str(one_th) + "천원"

    print(f"▷ 현재 남은 돈은 {ten_th}만{one_th}입니다.")


print("1. 시작 용돈은 30만원입니다.")
money = 300000
calMoney(money)

print("2. 개강 첫 주, 8만원 도서 구입하였습니다.")
money -= 80000
calMoney(money)

print("3. 평일 점심 값 매일 9천원씩 5일간 지출하였습니다.")
money -= (9000 * 5)
calMoney(money)

money += 120000
print("4. 과외 아르바이트로 인해 12만원 수입이 생겼습니다.")
calMoney(money)

print("5. 부모님의 용돈으로 현재 급액의 20% 추가 수입이 생겼습니다.")
money *= 1.2
money = int(money)
calMoney(money)

print("6. 전기요금 등의 공과금으로 남은 돈의 1/3 지출하였습니다.")
money *= 2/3
money = int(money)
calMoney(money)


