# 복합대입 연산자
'''
산술 연산자와 대입 연산자(=)를 결합한 연산자
- += : 연산자 계산 후 대입
- =+ : 연산자 대입 후 계산
'''
a = 10
a + 3
print(a)  # 10

a = a + 3
print(a)  # 13

a = 10
a += 5
print(a)  # 15

a = 10
a -= 3
print(a)  # 7

a = 10
a *= 2
print(a)  # 20

a = 10
a /= 4
print(a)  # 2.5

a = 10
a //= 5
print(a)  # 2

a = 10
a %= 2
print(a)  # 0

a = 10
a **= 3
print(a)  # 1000

print("=== 복합 할당 연산자 동작 예제 ===")

a = 10                          # 초기값 설정
print(f"초기값: a = {a}")

a += 5                          # a = a + 5 = 10 + 5 = 15
print(f"a += 5 → a = {a}")       # 15

a -= 4                          # a = a - 4 = 15 - 4 = 11
print(f"a -= 4 → a = {a}")       # 11

a *= 3                          # a = a * 3 = 11 * 3 = 33
print(f"a *= 3 → a = {a}")       # 33

a /= 11                         # a = a / 11 = 33 / 11 = 3.0
print(f"a /= 11 → a = {a}")      # 3.0 (실수형)

a //= 2                         # a = a // 2 = 3.0 // 2 = 1.0
print(f"a //= 2 → a = {a}")      # 1.0 (정수 나눗셈, 소수점 버림)

a **= 200                       # a = a ** 200 = 1.0 ** 200 = 1.0
print(f"a **= 200 → a = {a}")    # 1.0 (1의 어떤 거듭제곱도 1)

a = 10                          # 새로운 값으로 재설정
a %= 3                          # a = a % 3 = 10 % 3 = 1
print(f"a = 10, a %= 3 → a = {a}")  # 1 (10을 3으로 나눈 나머지)

