# 연산자
'''
연산자(Operator) : 값과 값 사이에 연산(계산)을 수행하는 기호 또는 키워드
항(Operand) : 연산자의 좌우에 위치하여 연산의 대상이 되는 값
'''

'''
1. 대입 연산자
    우측의 값을 좌측 변수에 저장(할당)
    우측의 값이 먼저 계산된 후, 그 결과가 좌측 변수에 저장

2. 산술 연산자
    산술 연산자는 숫자에 대해 수학적 연산을 수행
'''

######################################################################################
# 연산자 우선순위

result = 2 + 3 * 4
print(result)

result = (2 + 3) * 4
print(result)

######################################################################################
# 변수와 사칙연산

a = 20
b = 6

# 덧셈
sum_result = a + b
print("1. 덧셈 (a + b):", sum_result)

# 뺄셈
sub_result = a - b
print("2. 뺼셈 (a - b):", sub_result)

# 곱셈
mul_result = a * b
print("3. 곱셈 (a * b):", mul_result)

# 나눗셈 (실수형 결과)
div_result = a / b
print("4. 나눗셈 (a / b):", div_result)

# 정수 나눗셈 (몫)
floor_div_result = a // b
print("5. 정수 나눗셈 (a // b):", floor_div_result)

# 나머지
mod_result = a % b
print("6. 나머지 (a % b):", mod_result)

# 거듭제곱
exp_result = a ** 2
print("7. 거듭제곱 (a ** 2):", exp_result)

