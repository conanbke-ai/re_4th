# 실습 5 발표 순서와 발표 주제 정하기
'''
한 조에서 팀 프로젝트 발표 순서와 발표 주제를 정했습니다.
    발표자 이름 3명과 주제 3개를 한 번에 입력 받고, 아래와 같이 출력해보세요.
'''

name1, name2, name3 = input("발표자 이름 3명을 입력해주세요 \n : ").split()
sub1, sub2, sub3 = input("발표 주제 3개를 입력해주세요 \n : ").split()

print('발표 순서 안내입니다. \n', f'1조 발표자: {name1} - 주제: {sub1}',
      f'2조 발표자: {name2} - 주제: {sub2}', f'3조 발표자: {name3} - 주제: {sub3}', sep="\n")

