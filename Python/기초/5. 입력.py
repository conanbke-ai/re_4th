# 실습 4 입력과 연산 연습하기
'''
1. 사용자로부터 가로과 세로를 입력 받아 넓이와 둘레를 계산하세요.
    넓이 : 200
    둘레 : 60

2. 네 자릿수 정수를 입력 받고, 각 자릿수를 분리하여 출력하세요.
    천의 자리 : 2
    백의 자리 : 1
    십의 자리 : 4
    일의 자리 : 5
'''

# 가로, 세로 길이 입력
width, hight = map(int, input("가로와 세로 길이를 입력해주세요 \n : ").split())
# 넓이
Area = width * hight
# 둘레
Perimeter = 2 * (width + hight)

print(f"""넓이 : {Area}
둘레 : {Perimeter}""")

# 네 자릿수 정수 입력
num = input("네 자릿수 정수를 입력해주세요 \n : ")
num = int(num)
th = num // 1000
hun = (num - (th*1000)) // 100
ten = (num - (th*1000) - (hun*100)) // 10
one = (num - (th*1000) - (hun*100) - (ten*10))
print(f"""천의 자리: {th}
백의 자리: {hun}
십의 자리: {ten}
일의 자리: {one}""")

