# 실습 6 날짜와 시간
'''
년, 월, 일과 시, 분, 초를 한번에 입력 받아서 출력하기
- 첫번째 입력에서 년, 월, 일 입력 (.으로 구분)
- 두번째 입력에서 시, 분, 초 입력 (:으로 구분)

아래와 같이 출력
RE4의 개강일은 2025년 09월 03일
시작 시간은 09시 00분 00초 입니다.
'''

year, month, day = input("년, 월, 일을 입력해주세요. .으로 구분합니다 \n : ").split(".")
hour, min, sec = input("시, 분, 초를 입력해주세요. :으로 구분합니다 \n : ").split(":")

if (int(month) < 10):
    month = '0' + month
if (int(day) < 10):
    day = '0' + day
if (int(hour) == 0 or int(hour) < 10 and hour != '00'):
    hour = '0' + hour
if (int(min) == 0 or int(min) < 10 and min != '00'):
    min = '0' + min
if (int(sec) == 0 or int(sec) < 10 and sec != '00'):
    sec = '0' + sec
print(f'RE4의 개강일은 {year}년 {month}월 {day}일',
      f'시작 시간은 {hour}시 {min}분 {sec}초 입니다.', sep='\n')
