# 입력 : input() 함수
'''
사용자로부터 콘솔을 통해 문자열 형태의 입력을 받는 함수
입력은 항상 문자열(str) 형태로 반환임 → 숫자 계산을 위해서는 형 변환 필요
'''
name = input("이름을 입력하세요 \n" + ": ")
print(name)

score = input("점수를 입력하세요 \n" + ": ")
print(f'점수는  : {score}')

# split() : 문자열을 구분자로 나누는 함수 (list로 나뉘어짐)
'''
문자열만 사용 가능한 함수(숫자 사용 불가)
split 함수 괄호 안에 구분자 지정 가능 / 기본값 : 공백
'''
fruit = "사과 참외 수박".split()
print(fruit)

fruit1, fruit2, fruit3 = input().split()
print(fruit1, fruit2, fruit3, sep=", ")

